      <h2>Types</h2>
      <p>Many institutions make a distinction between a circulating or lending library, where materials are expected and intended to be loaned to patrons, institutions, or other libraries, and a reference library where material is not lent out. Travelling libraries, such as the early horseback libraries of eastern Kentucky[14] and bookmobiles, are generally of the lending type. Modern libraries are often a mixture of both, containing a general collection for circulation, and a reference collection which is restricted to the library premises. Also, increasingly, digital collections enable broader access to material that may not circulate in print, and enables libraries to expand their collections even without building a larger facility. Lamba (2019) reinforced this idea by observing that "today’s libraries have become increasingly multi-disciplinary, collaborative and networked" and that applying Web 2.0 tools to libraries would "not only connect the users with their community and enhance communication but will also help the librarians to promote their library’s activities, services, and products to target both their actual and potential users".</p>
